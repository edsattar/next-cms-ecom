"use client";

import { Plus } from "lucide-react";
import { useParams, useRouter } from "next/navigation";

import { Button } from "@/components/ui/button";
import { DataTable } from "@/components/ui-c/data-table";
import { Heading } from "@/components/ui-c/heading";
import { Separator } from "@/components/ui/separator";

import { CategoryColumns, CategoryColumnsType } from "./CategoryColumns";
import { ApiList } from "@/components/ui-c/api-list";

interface Props {
  data: CategoryColumnsType[];
}

export const CategoryClient = ({ data }: Props) => {
  const params = useParams();
  const router = useRouter();

  return (
    <>
      <div className="flex items-center justify-between">
        <Heading
          title={`Categories (${data.length})`}
          description="Manage categories for your store"
        />
        <Button
          onClick={() => router.push(`/${params.storeId}/categories/new`)}
        >
          <Plus className="mr-2 h-4 w-4" /> Add New
        </Button>
      </div>
      <Separator />
      <DataTable filterKey="name" columns={CategoryColumns} data={data} />
      <Heading title="API" description="API Calls for Categories" />
      <Separator />
      <ApiList entityName="categories" entityIdName="categoryId" />
    </>
  );
};
